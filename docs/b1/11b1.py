print("operador ==")
print(5 == 7) #respuesta false
print(5 == 5) #respuesta true
print(5.0 == 5, "\n") #respuesta true

print("operador !=")
print(5 != 7) #respuesta true
print(5 != 5) #respuesta false
print(5.0 != 5, "\n") #respuesta false

print("operador >")
print(5 > 7) #respuesta false
print(5 > 5) #respuesta false
print(5.0 > 5, "\n") #respuesta false

print("operador <")
print(5 < 7) #respuesta true
print(5 < 5) #respuesta false
print(5.0 < 5, "\n") #respuesta false

print("operador >=")
print(5 >= 7) #respuesta false
print(5 >= 5) #respuesta true
print(5.0 >= 5, "\n") #respuesta true

print("operador <=")
print(5 <= 7) #respuesta true
print(5 <= 5) #respuesta true
print(5.0 <= 5) #respuesta true
